                    Um exemplo interessante dessa saudável mistura é o próprio Rock in Rio, cuja programação é iniciada meses antes do evento presencial e conta com uma plataforma virtual e cursos presenciais focados em Gestão de Eventos. <strong>Clique no vídeo para entender mais.</strong>
