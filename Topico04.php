                <p class="Texto">
                    Mas afinal, qual a tipologia do Rock In Rio? Festival, Curso ou Educação? Tudo isso junto? <strong>O festival atualmente se posiciona como uma plataforma, uma experiência que permeia vários formatos e canais.</strong>
                </p>

                <p class="Texto">
                    A inspiração nesse projeto é válida, mas é preciso delimitar o alcance do seu evento e ter um foco claro. O seu evento será um curso? Um fórum? Uma mistura de conferência e convenção?
                </p>

                <p class="Texto">
                    Assim como Rock in Rio, seu <strong>evento deve estar mais preocupado com a experiência do que as tipologias</strong>, mas é fundamental conhecê-las e se apropriar das possibilidades já existentes e herdadas da modalidade presencial.
                </p>

                <p class="Texto">
                    Cada tipo de evento tem suas regras próprias que podem ser pesquisadas em sites ou livros específicos. O propósito desse curso é imersar no universo de eventos digitais, da criação à execução. Por isso, abordaremos às tipologias de forma resumida.
                </p>

                <p class="Texto">
                    <strong>O tempo e a dinâmica deve ser adaptados para o ambiente digital</strong>. Um seminário presencial de uma hora, por exemplo, pode ser adaptado para quarenta minutos ou menos, seguido por perguntas e respostas.
                </p>

                <p class="Texto">
                    É bastante comum o <strong>uso inadequado de tipologias nos eventos presenciais e digitais</strong>, então decidimos listar algumas tipologias que podem ser utilizadas em eventos educacionais.
                </p>

                <p class="Texto">
                    <strong>Note que algumas tipologias são sociais ou culturais</strong>. São categorias não relacionadas diretamente com a educação, mas que podem fazer parte do evento em momentos específicos através de intervenções artísticas.
                </p>

                <p class="Texto">
                    Resumimos e esclarecemos algumas tipologias com base em (Zitta, 2014):
                </p>

                <div class="container">
                    <div class="table-responsive">
                        <table class="table table-bordered">
                            <thead>
                                <tr>
                                    <th>Tipologia</th>
                                    <th>Descritivo</th>
                                </tr>
                            </thead>
                            <tbody class="tBody">
                                <tr>
                                    <th class="left">Debate</th>
                                    <th>O debate acontece quando o tema sucinta posições contrárias ou mesmo quando fortes dúvidas formam blocos de opiniões divergentes.<br>
                                        Ele pode acontecer no decorrer de uma discussão, quando o conceito, uma apreciação ou tese não alcança unanimidade. Neste caso, os elementos se voltam para defender os próprios pontos de vista.<br>
                                        Assim, o método de debate é uma competição intelectual em grupo, quando uns procuram demonstrar a superioridade das suas teses sobre às dos outros.<br>
                                        O método exige conhecimento prévio sobre o tema. <strong>Do contrário, ter-se-á uma troca inconsequente de argumentos, podendo chegar a ofensas pessoais</strong>. Assim, o método leva a argumentar de forma lógica os prós e contras de uma tese.
                                    </th>
                                </tr>
                                <tr>
                                    <th class="left">Mesa-redonda</th>
                                    <th>É a reunião de especialistas, sustentando posições divergente a respeito de um tema. Grupo de pessoas especializadas em determinado assunto aprenda e expõe pontos de vista divergentes ou contraditórios sob o mesmo tema.<br>
                                        <strong>Visa dar ao auditório a oportunidade de conhecer o pensamento de técnicos ou especialistas sobre um assunto controvertido.</strong>
                                </tr>
                                <tr>
                                    <th class="left">Fórum</th>
                                    <th>Entende-se como uma reunião grupo da qual todos os presentes em uma sala, reunião ou grupo tem a oportunidade de participar ativamente. É organizado com a finalidade de debater um tema ou problema. </th>
                                </tr>
                                <tr>
                                    <th class="left">Simpósio</th>
                                    <th>É o método onde duas ou mais pessoas, versadas em determinado assunto, sob a direção de um coordenador, expõe um tema sobre sua especialidade, cada qual apresentando uma parte ou focalizando seus pontos de vista diferentes, contando com a participação do auditório no final da sessão por meio de perguntas. </th>
                                </tr>
                                <tr>
                                    <th class="left">Seminário</th>
                                    <th>Sua finalidade é a investigação ou estudo de um tema em reuniões de trabalho devidamente planejados. É um grupo de trabalho ativo, pois os membros não recebem às informações elaboradas. Pesquisam por seus próprios meios em colaboração recíproca. </th>
                                </tr>
                                <tr>
                                    <th class="left">Painel</th>
                                    <th>É uma reunião de pessoas especializadas expondo ideias sobre um determinado assunto ao auditório de maneira informal e dialogada em troca de ideias, expondo e apreciando diferentes perspectivas. </th>
                                </tr>
                                <tr>
                                    <th class="left">Brainstorming</th>
                                    <th>Um grupo se reúne em assembleia quando tem por finalidade encontrar ideias ou soluções novas, sendo necessário o uso da imaginação. O objetivo é produzir o maior número de ideias sobre um tema ou problema. </th>
                                </tr>
                                <tr>
                                    <th class="left">Entrevista Coletiva</th>
                                    <th>Evento em que autoridade de uma instituição, ou seu representante, comunica à imprensa ou aos seus profissionais um fato novo de alta relevância, abrindo, em seguida, espaço para questionamentos. </th>
                                </tr>
                                <tr>
                                    <th class="left">Exposição</th>
                                    <th>Evento em que há um “cenário” cuidadosamente preparado para apresentação de resultados de uma empresa ou instituição e atender aos questionamento do público visitante. Podem ser agropecuárias, industriais, comerciais, informática etc.</th>
                                </tr>
                                <tr>
                                    <th class="left">Mostra</th>
                                    <th>Evento onde a empresa apresenta resultados de trabalhos, documentos históricos ou material artístico/cultural produzido por colaboradores ou convidados. </th>
                                </tr>
                                <tr>
                                    <th class="left">Lançamento</th>
                                    <th>Evento utilizado para apresentar novas tecnologias, serviços e produtos ou novidades que tragam contribuições significativas à sociedade.</th>
                                </tr>
                                <tr>
                                    <th class="left">Pedra Fundamental</th>
                                    <th>Evento que marca o início de uma obra. </th>
                                </tr>
                                <tr>
                                    <th class="left">Palestra</th>
                                    <th>Evento caracterizado pela apresentação de um tema, por um ou mais especialistas, a um grupo de pessoas com interesses comuns. </th>
                                </tr>
                                <tr>
                                    <th class="left">Curso</th>
                                    <th>É a capacitação de pessoas para execução de tarefas específicas. O objetivo do evento é mais teórico do que prático, porém não invalida a prática. É indicado para pessoas que têm baixo ou nenhum conhecimento sobre o assunto. Com exceção dos cursos de especialização, que têm o objetivo de aperfeiçoar o conhecimento de quem já domina o assunto.</th>
                                </tr>
                                <tr>
                                    <th class="left">Workshop</th>
                                    <th>É o termo em inglês para definir oficina de trabalho. É um evento dividido em: teoria e prática. Os participantes devem ser da mesma área ou segmento de trabalho e se reúnem para debater, praticar e procurar soluções para o tema proposto. </th>
                                </tr>
                                <tr>
                                    <th class="left">Congresso</th>
                                    <th>É uma reunião formal e periódica de profissionais atuando na mesma área, promovida por entidades associativas e inclui debates e diversas outras tipologias. </th>
                                </tr>
                                <tr>
                                    <th class="left">Encontro</th>
                                    <th>Evento onde pessoas da mesma categoria profissional e interesses comuns debates temas polêmicos. Pode ser estadual, regional, nacional ou internacional.</th>
                                </tr>
                                <tr>
                                    <th class="left">Jornada</th>
                                    <th>São encontros de grupos profissionais mais próximos, de âmbito regional, que discutem, periodicamente, assuntos de interesse do grupo. As conclusões podem definir linhas norteadoras para os trabalhos em grupo e para as profissões. Jornadas podem durar vários dias, isso depende da complexidade e do tamanho do evento.</th>
                                </tr>
                                <tr>
                                    <th class="left">Feira</th>
                                    <th>Evento onde, em um estande, a empresa apresenta os resultados de seu trabalho e comercializa suas tecnologias, produtos e serviços. </th>
                                </tr>
                                <tr>
                                    <th class="left">Rodada de Negócios</th>
                                    <th>Evento onde se reúnem empresários de um determinado segmento, devidamente inscritos, para vender ou comprar produtos e serviços.</th>
                                </tr>
                                <tr>
                                    <th class="left">Convenção</th>
                                    <th>Reunião de pessoas de departamentos ou seções de uma empresa objetivando: conhecer novas diretrizes, treinamento, reciclagem, entrosamento, troca de experiências e informações. </th>
                                </tr>
                                <tr>
                                    <th class="left">Semana</th>
                                    <th>Pode ser de dois tipos: acadêmica (estudantes coordenados por professores e apoiados por profissionais da área do mesmo segmento) ou empresarial (reunião de pequenos eventos empresariais tematizados). </th>
                                </tr>
                                <tr>
                                    <th class="left">Conferência</th>
                                    <th>Caracteriza-se pela apresentação de um tema informativo geralmente técnico ou científico por uma autoridade em determinado assunto para um grande número de pessoas. </th>
                                </tr>
                                <tr>
                                    <th class="left">Colóquio</th>
                                    <th>Semelhante à conferência, apresentado por conferencista de renome e expert no assunto. Possui objetivo de esclarecimento de um tema ou tomada de decisão. Utilizado mais em classes específicas, por exemplo, classe médica.
                                    </th>
                                </tr>
                                <tr>
                                    <th class="left">Reuniões</th>
                                    <th>Reúne grupos de pessoas na empresa ou de outras instituições para discutir temas relacionados com suas atividades administrativas e políticas. </th>
                                </tr>
                                <tr>
                                    <th class="left">Posse</th>
                                    <th>Realizado em auditório sentado ou em salão em pé.</th>
                                </tr>
                                <tr>
                                    <th class="left">Salão</th>
                                    <th>Até algum tempo atrás o conceito de salão era semelhante ao de feira, mas menos amplo, sem objetivo de venda ou mercadológicos. Atualmente é considerado um sinônimo de feira e pode ser realizado por empresas diferentes no mesmo espaço e segmento de mercado. </th>
                                </tr>
                                <tr>
                                    <th class="left">Inauguração</th>
                                    <th>Apresentação da empresa ou instituição ao público-alvo, entidade ou governo. Envolve um cerimonial específico.</th>
                                </tr>
                                <tr>
                                    <th class="left">Torneio</th>
                                    <th>Possui caráter esportivo e competitivo. Geralmente é mais completo, envolve várias etapas até a definição de quem será o vencedor. </th>
                                </tr>
                                <tr>
                                    <th class="left">Gincana </th>
                                    <th>Competição organizada por uma comissão que define as tarefas a serem realizadas pelas equipes e regras para definição de vendedores. </th>
                                </tr>
                                <tr>
                                    <th class="left">Show</th>
                                    <th>Encontro artístico com foco em música, caracterizado pela participação do público-alvo em um evento essencialmente de entretenimento.</th>
                                </tr>
                                <tr>
                                    <th class="left">Talk-show</th>
                                    <th>Evento que associa uma entrevista pessoal com artista ou profissional de renome acompanhado por atração musical. </th>
                                </tr>
                                <tr>
                                    <th class="left">Sarau / Lual</th>
                                    <th>Evento composto por amigos, textos, músicas em um local descontraído com petiscos. </th>
                                </tr>
                                <tr>
                                    <th class="left">Vernissage</th>
                                    <th>Evento demonstrativo caracterizado pelo lançamento conceitual e comercial de peças, itens ou coleções artísticas, individuais ou coletivas.
                                    </th>
                                </tr>
                            </tbody>
                        </table>
